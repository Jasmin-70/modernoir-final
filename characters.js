
function playAudio(audioId) {
    var audio = document.getElementById(audioId);
    if (audio) {
        audio.currentTime = 0; // Reset audio to the beginning
        audio.play();
    }
}

// Function to pause audio (optional, but good for short effects)
function pauseAudio(audioId) {
    var audio = document.getElementById(audioId);
    if (audio) {
        audio.pause();
        audio.currentTime = 0; // Reset after pause
    }
}

document.getElementById('hoverDiv').addEventListener('mouseover', function() {
    playAudio('hoverAudio');
});
document.getElementById('hoverDiv').addEventListener('mouseout', function() {
    pauseAudio('hoverAudio');
});

